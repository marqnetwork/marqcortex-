// ============================================================================
// DEPENDENCY TRACER — follow demands upstream / supplies downstream
// ============================================================================

/** Returns all nodes that a given node depends on (direct + transitive) */
export function traceUpstream(id: string, visited = new Set<string>()): RegistryNode[] {
  if (visited.has(id)) return [];
  visited.add(id);
  const node = REGISTRY_BY_ID[id];
  if (!node) return [];
  const direct = node.demands
    .map(d => REGISTRY_BY_ID[d])
    .filter(Boolean) as RegistryNode[];
  const transitive = direct.flatMap(d => traceUpstream(d.id, visited));
  return [...direct, ...transitive];
}

/** Returns all nodes that depend on (demand) a given node */
export function traceDownstream(id: string): RegistryNode[] {
  return REGISTRY.filter(node => node.demands.includes(id) || (node.apiCalls || []).includes(id));
}

/** Returns the complete dependency chain as an ordered path */
export function getDependencyPath(fromId: string, toId: string): string[] {
  const visited = new Set<string>();
  const path: string[] = [];
  function dfs(currentId: string): boolean {
    if (visited.has(currentId)) return false;
    visited.add(currentId);
    path.push(currentId);
    if (currentId === toId) return true;
    const node = REGISTRY_BY_ID[currentId];
    if (!node) return false;
    for (const dep of node.demands) {
      if (dfs(dep)) return true;
    }
    path.pop();
    return false;
  }
  dfs(fromId);
  return path;
}

