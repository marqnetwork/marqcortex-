/**
 * ============================================================================
 * MARQ CORTEX — SYSTEM REGISTRY  (MQC-REGISTRY v1.0)
 * ============================================================================
 *
 * EVERY file, component, engine, hook, service, API route, context, and
 * utility in the codebase is assigned a stable unique ID here.
 *
 * ID FORMAT:  MQC-{TYPE}-{NNN}
 *
 * TYPE CODES:
 *   APP = Application entry        CFG = Config file
 *   RTE = Frontend route           CMP = React component
 *   CTX = React context            HKS = React hook
 *   SVC = Service (data layer)     ENG = Core engine module
 *   UTL = Utility module           TYP = Type definition file
 *   LIB = Library wrapper          API = Backend API route
 *   BEF = Backend file             UI  = UI primitive (shadcn)
 *
 * HOW TO USE THIS DURING DEBUGGING:
 *   1. Identify the symptom (blank page, wrong data, etc.)
 *   2. Find the node by name or path (search in RegistryViewer)
 *   3. Note its ID and check its `demands` list
 *   4. Follow the demand chain until you find the root cause
 *   5. Check `debugNotes` and `recentFixes` for known patterns
 *   6. Report fixes back here so the registry stays current
 * ============================================================================
 */

export type NodeType =
  | 'APP'   // Application entry point
  | 'CFG'   // Configuration file
  | 'RTE'   // Frontend route/page
  | 'CMP'   // React component
  | 'CTX'   // React context
  | 'HKS'   // React hook
  | 'SVC'   // Service (data access layer)
  | 'ENG'   // Deterministic core engine
  | 'UTL'   // Utility module
  | 'TYP'   // Type definitions
  | 'LIB'   // Library/API wrapper
  | 'API'   // Backend API route (Hono/Supabase Edge)
  | 'BEF'   // Backend file (non-route)
  | 'UI';   // UI primitive (shadcn/ui)

export type NodeStatus =
  | 'stable'     // Working correctly, no known issues
  | 'watch'      // Had recent bugs — monitor carefully
  | 'hot'        // Active known issue or under repair
  | 'protected'  // System file — DO NOT modify
  | 'demo-only'; // Only functional in demo/mock mode

export type NodeDomain =
  | 'app-root'
  | 'config'
  | 'public-funnel'
  | 'team-auth'
  | 'team-dashboard'
  | 'client-portal'
  | 'ai-cortex'
  | 'data-layer'
  | 'backend-core'
  | 'backend-ai'
  | 'backend-email'
  | 'backend-infra'
  | 'ui-system'
  | 'types'
  | 'analytics'
  | 'roi-engine'
  | 'proposal-system'
  | 'execution-system';

export interface RegistryNode {
  id: string;
  label: string;
  path: string;
  type: NodeType;
  domain: NodeDomain;
  description: string;
  demands: string[];       // IDs of nodes this imports / depends on
  supplies: string[];      // Named exports this node provides
  apiCalls?: string[];     // API route IDs called by this node
  keyFunctions?: string[]; // Key exported functions/components
  debugNotes?: string;     // Gotchas, caveats, known patterns
  recentFixes?: string[];  // Recent bug fixes applied here
  status: NodeStatus;
}

